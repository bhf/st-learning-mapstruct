package com.bhf.learning.mapstruct.iterableexample;

public class ItemDTO {
    private String value;

    public ItemDTO() {
    }

    public ItemDTO(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
